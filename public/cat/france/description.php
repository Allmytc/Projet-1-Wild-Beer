    <h2>Les bières Française</h2>
    <p>
    LIl faudra attendre la fin du Moyen Âge pour voir
    l'utilisation du houblon prendre de l'ampleur.
     Plusieurs auteurs rapportent que Jean sans Peur,
    duc de Bourgogne, aurait créé l'ordre du Houblon pour favoriser la culture de cette plante
    et distribuer des récompenses à ces producteurs.
    Le houblon était d'ailleurs l'un de ses emblèmes,
    attesté dès 1392 à l'occasion du voyage de Bretagne.

    <br/>Un édit en 1435 impose cette recette et le mot bière apparaît
    pour la première fois dans une ordonnance rendue le 1er avril 1435
     par Jacques d'Estouville, prévôt de Paris, sous le règne de Charles VII,
      ordonnance qui réglemente le commerce des cervoises </p>

